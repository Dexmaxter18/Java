public class PrimitivoBoolean {
	public static void main(String[] args){
//----------------------------------------boolean--------------------------------------------//
//La variable de tipo boolean devuelve dos valores true si lo que se le pasa es verdadero (por ejemplo una suma) o false si lo que se le pasa no lo es
		boolean esDiferente = true;
		int primero = 1;
		int segundo = 3;
		esDiferente = primero < segundo;
		System.out.println(esDiferente);
		boolean esIgual = 3 - 1 == 2;
		System.out.println(esIgual);
	}
}
